const TAM = 5

//FOR
for (let i = 1; i <= TAM; i++) {
    if (i % 2 == 1){
        console.log(i, 'Estou aprendendo!!!')
    }
}
console.log('\n')

//WHILE
var i = 1
while (i <= TAM) {
    if (i % 2 == 1){
        console.log(i, 'Estou aprendendo!!!')
    }
    i ++
}
console.log('\n')

//DO WHILE
var i = 1
do {
    if (i % 2 == 1){
        console.log(i, 'Estou aprendendo!!!')
    }
    i ++
}while (i <= TAM)